if (!function_exists('display_select')) {
    function display_select($tab, $name, $multiple = 0, $value = 0)
    {
        $ret = '<select name="'.$name.'" style="width:190px;"';
        if ($multiple)
            $ret .= 'multiple="multiple" size="'.sizeof($tab['designation']).'"';
        $ret .= '>';
        for ($i = 0; $i < sizeof($tab['designation']); $i++)
        {
            $ret .= '<option value="'.$i.'"';
            if ($i == $value) { $ret .= ' selected'; }
            $ret .= '>'.$tab['designation'][$i].'</option>';
        }
        $ret .= '</select>';

        return $ret;
    }
}

